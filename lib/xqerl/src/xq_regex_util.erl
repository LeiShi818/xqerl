-module(xq_regex_util).

%% ====================================================================
%% API functions
%% ====================================================================
-export([range/1]).
-export([range_to_list/1]).
-export([range_to_set/1,
         range_to_regex/1,
         set_to_range/1]).
-export([intersection/2,
         union/2,
         subtract/2,
         symmetric_diff/2]).

range_to_list({range,Min,Max}) ->
   lists:seq(Min, Max);
range_to_list({value,Val}) ->
   [Val].


range_to_set(Range) ->
   % pmap-ed due to huge lists
   Nums = lists:append(
            xqerl_seq3:pmap(fun range_to_list/1, Range, 16)
            ),
   sets:from_list(Nums).

range_to_regex(Range) ->
   Fun = fun({value,Val},Acc) ->
               hex(Val) ++ Acc;
            ({range,Min,Max},Acc) ->
               hex(Min) ++ "-" ++ hex(Max) ++ Acc
         end,
   lists:foldr(Fun, "", Range).

hex(Int) ->
   "\\x{"++integer_to_list(Int,16)++"}".

set_to_range(Set) ->
   Fun = fun(C,{[],[],Acc}) ->
               {C,C,Acc};
            (C,{First,Last,Acc}) when C =:= Last + 1 ->
               {First,C,Acc};
            (C,{First,First,Acc}) ->
               New = {value,First},
               {C,C,[New|Acc]};
            (C,{First,Last,Acc}) ->
               New = {range,First,Last},
               {C,C,[New|Acc]}
         end,
   {F,L,Range} = lists:foldl(Fun, {[],[],[]}, lists:sort(sets:to_list(Set))),
   if F =:= [] ->
         [];
      F =:= L ->
         lists:reverse([{value,F}|Range]);
      true ->
         lists:reverse([{range,F,L}|Range])
   end.

% A∩B
intersection(Set1,Set2) ->
   sets:intersection(Set1, Set2).
% A∪B
union(Set1,Set2) ->
   sets:union(Set1, Set2).
% A-B
subtract(Set1,Set2) ->
   sets:subtract(Set1, Set2).
% A⊖B
symmetric_diff(Set1,Set2) ->
   SetU = sets:union(Set1, Set2),
   SetI = sets:intersection(Set1, Set2),
   sets:subtract(SetU, SetI).


