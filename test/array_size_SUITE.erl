-module('array_size_SUITE').
-include_lib("common_test/include/ct.hrl").
-export([all/0,
         suite/0]).
-export([init_per_suite/1,
         end_per_suite/1]).
-export(['array-size-001'/1]).
-export(['array-size-002'/1]).
-export(['array-size-003'/1]).
-export(['array-size-004'/1]).
-export(['array-size-005'/1]).
-export(['array-size-006'/1]).
