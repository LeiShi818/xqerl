'seq-queries-results-q1'(Config) ->
   BaseDir = ?config(base_dir, Config),
   Qry = "for $s in //section[section.title = \"Procedure\"] return ($s//incision)[2]/instrument",
   {Env,Opts} = xqerl_test:handle_environment(environment('report1',BaseDir)),
   Qry1 = lists:flatten(Env ++ Qry),
   io:format("Qry1: ~p~n",[Qry1]),
   Res = try Mod = xqerl_module:compile(filename:join(BaseDir, "seq-queries-results-q1.xq"), Qry1),
             xqerl:run(Mod,Opts) of D -> D catch _:E -> E end,
   Out =    case xqerl_test:assert_xml(Res,"<instrument>electrocautery</instrument>") of 
      true -> {comment, "XML Deep equal"};
      {false, F} -> F 
   end, 
   case Out of
      {comment, C} -> {comment, C};
      Err -> ct:fail(Err)
   end.
